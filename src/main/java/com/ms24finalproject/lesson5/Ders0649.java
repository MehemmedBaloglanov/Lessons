package com.ms24finalproject.lesson5;

public class Ders0649 {
    public static void main(String[] args) {
        /*
        Karsilastirma operatorleri

        == --> Eger degerler esitse true dondurur
        != --> Eger degerler esit degilse true dondurur
        < --> Eger degerlerin ilk degeri buyukse true dondurur
        > --> Eger degerlerin ilk degeri buyuk degilse true dondurur
        <= --> Eger degerlerin ilk degeri buyuk veya esitse true dondurur
        >= --> Eger degerlerin ilk degeri buyuk veya esit degilse true dondurur
         */

        System.out.println(3 == 3);
        System.out.println(3 != 4);
        System.out.println(5 < 3);
        System.out.println(5 > 3);
        System.out.println(5 <= 5);
        System.out.println(5 >= 5);

        System.out.println("Mehmet" == "Mehmet");
    }
}
